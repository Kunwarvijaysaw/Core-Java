package Oct6HasARelation;

class Address {
	String streetName;
	String city;
	String state;
	long pinCode;
	String country;
	public Address(String streetName, String city, String state, long pinCode, String country) {
		
		this.streetName = streetName;
		this.city = city;
		this.state = state;
		this.pinCode = pinCode;
		this.country = country;
	}
	


}
